package com.clover.InterfaceCallBack;

public class Weather {

    //城市
    private String city;
    //天气信息
    private String msg;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
